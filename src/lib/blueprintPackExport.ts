import type { BlueprintPack, BlueprintSheet } from './blueprintSheetTypes';

// ---- JSON Export ----
export function exportBlueprintPackJson(pack: BlueprintPack): string {
  return JSON.stringify(pack, null, 2);
}

// ---- Markdown Export ----
export function exportBlueprintPackMarkdown(pack: BlueprintPack): string {
  const lines: string[] = [];
  lines.push(`# Blueprint Pack — ${pack.projectName}`);
  lines.push(`**Generated:** ${new Date(pack.generatedAt).toLocaleString()}`);
  lines.push(`**Revision:** ${pack.revision}`);
  lines.push(`**Status:** ${pack.status}`);
  lines.push(`**Template:** ${pack.templateName || "Custom"}`);
  lines.push("");
  lines.push("---");
  lines.push("");

  // Summary
  lines.push("## Pack Summary");
  lines.push(`| Metric | Value |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Total Sheets | ${pack.summary.totalSheets} |`);
  lines.push(`| Generated | ${pack.summary.generatedSheets} |`);
  lines.push(`| Missing Data | ${pack.summary.missingDataSheets} |`);
  lines.push(`| Warnings | ${pack.summary.warnings} |`);
  lines.push(`| Errors | ${pack.summary.errors} |`);
  lines.push(`| Blockers | ${pack.summary.blockers} |`);
  lines.push("");

  // Each sheet
  pack.sheets.forEach((sheet) => {
    lines.push("---");
    lines.push("");
    lines.push(`## Sheet ${sheet.sheetNo}: ${sheet.title}`);
    lines.push(`**Category:** ${sheet.category} | **Status:** ${sheet.status}`);
    lines.push("");

    // Notes
    if (sheet.notes.length > 0) {
      lines.push("### Notes");
      sheet.notes.forEach(n => lines.push(`- ${n}`));
      lines.push("");
    }

    // Tables
    sheet.tables.forEach(table => {
      lines.push(`### ${table.title}`);
      if (table.columns.length > 0) {
        lines.push(`| ${table.columns.join(" | ")} |`);
        lines.push(`| ${table.columns.map(() => "---").join(" | ")} |`);
        table.rows.forEach(row => {
          lines.push(`| ${row.join(" | ")} |`);
        });
      }
      lines.push("");
    });

    // Warnings
    if (sheet.warnings.length > 0) {
      lines.push("### Warnings");
      sheet.warnings.forEach(w => lines.push(`- **[${w.severity}]** ${w.title}: ${w.message}`));
      lines.push("");
    }

    // Disclaimer
    if (sheet.disclaimer) {
      lines.push(`> ⚠️ ${sheet.disclaimer}`);
      lines.push("");
    }

    // Source references
    if (sheet.sourceObjects.length > 0) {
      lines.push(`*Sources: ${sheet.sourceObjects.map(s => s.label || s.id).join(", ")}*`);
      lines.push("");
    }

    // Drawing summary
    const d = sheet.drawing;
    if (d.objects.length > 0) {
      lines.push(`*Drawing: ${d.objects.length} objects, ${d.connections.length} connections, ${d.dimensions.length} dimensions*`);
      lines.push("");
    }
  });

  lines.push("---");
  lines.push(`*Generated by Hardware Studio — ${new Date(pack.generatedAt).toISOString()}*`);
  return lines.join("\n");
}

// ---- HTML Export ----
function renderSheetHtml(sheet: BlueprintSheet): string {
  const statusClass = sheet.status === "Generated In App" ? "status-generated" :
    sheet.status === "Missing Data" ? "status-missing" :
    sheet.status === "Draft" ? "status-draft" : "status-review";

  let html = `<div class="sheet-page">`;
  
  // Title block
  html += `<div class="title-block">`;
  html += `<div class="title-row"><span class="sheet-no">Sheet ${sheet.sheetNo}</span><h2>${sheet.title}</h2><span class="status-badge ${statusClass}">${sheet.status}</span></div>`;
  html += `<div class="meta-row"><span>Category: ${sheet.category}</span></div>`;
  html += `</div>`;

  // Drawing summary
  const d = sheet.drawing;
  if (d.objects.length > 0) {
    html += `<div class="drawing-info"><em>${d.objects.length} drawing objects, ${d.connections.length} connections</em></div>`;
  }

  // Notes
  if (sheet.notes.length > 0) {
    html += `<div class="notes"><h3>Notes</h3><ul>`;
    sheet.notes.forEach(n => { html += `<li>${n}</li>`; });
    html += `</ul></div>`;
  }

  // Tables
  sheet.tables.forEach(table => {
    html += `<div class="table-section"><h3>${table.title}</h3><table><thead><tr>`;
    table.columns.forEach(c => { html += `<th>${c}</th>`; });
    html += `</tr></thead><tbody>`;
    table.rows.forEach(row => {
      html += `<tr>`;
      row.forEach(cell => { html += `<td>${cell}</td>`; });
      html += `</tr>`;
    });
    html += `</tbody></table></div>`;
  });

  // Warnings
  if (sheet.warnings.length > 0) {
    html += `<div class="warnings"><h3>Warnings</h3><ul>`;
    sheet.warnings.forEach(w => {
      const cls = w.severity === "Blocker" ? "blocker" : w.severity === "Error" ? "error" : w.severity === "Warning" ? "warning" : "info";
      html += `<li class="${cls}"><strong>[${w.severity}]</strong> ${w.title}: ${w.message}</li>`;
    });
    html += `</ul></div>`;
  }

  // Disclaimer
  if (sheet.disclaimer) {
    html += `<div class="disclaimer">⚠️ ${sheet.disclaimer}</div>`;
  }

  html += `</div><!-- end sheet-page -->`;
  return html;
}

