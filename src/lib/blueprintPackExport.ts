// ---- Summary JSON Export ----
export function exportBlueprintPackSummaryJson(pack: BlueprintPack): string {
  const summary = {
    id: pack.id,
    projectName: pack.projectName,
    generatedAt: pack.generatedAt,
    revision: pack.revision,
    status: pack.status,
    summary: pack.summary,
    sheets: pack.sheets.map(s => ({
      sheetNo: s.sheetNo,
      title: s.title,
      category: s.category,
      status: s.status,
      warningCount: s.warnings.length,
      tableCount: s.tables.length,
      drawingObjectCount: s.drawing.objects.length,
    })),
  };
  return JSON.stringify(summary, null, 2);
}
